#!/usr/bin/env tsx
/**
 * orchestration/bin/services.ts — `ollamas services`: the 25-µ-service registry runner.
 *
 * `--health` executes every service's REAL selftest ONE BY ONE (plus the 4 network health probes
 * unless --offline), streams the run as a live 25-item tracker checklist (watch it with
 * `ollamas follow`), prints per-service evidence, writes the machine-readable
 * SERVICE_REGISTRY.json, and exits non-zero if anything is red. `--list` prints the table only.
 *
 * Run:
 *   tsx orchestration/bin/services.ts --list
 *   tsx orchestration/bin/services.ts --health [--offline] [--json]
 */
import { writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { SERVICES, NETWORK_SERVICES, validateRegistry, healthRunEvents, type SelftestResult } from "./lib/services";
import { emitEvent, resetRun } from "./lib/tracker-io";

const HERE = dirname(fileURLToPath(import.meta.url));
const ORCH_DIR = join(HERE, "..");
const argv = process.argv.slice(2);
const JSON_OUT = argv.includes("--json");
const OFFLINE = argv.includes("--offline");
const LIST = argv.includes("--list");

interface Row { id: string; kind: string; ok: boolean; evidence: string; ms: number; }

async function probeNetwork(url: string): Promise<SelftestResult> {
  try {
    const res = await fetch(url, { signal: AbortSignal.timeout(3000) });
    return { ok: res.ok, evidence: `${res.status} ${url}` };
  } catch (e) { return { ok: false, evidence: `unreachable: ${(e as Error).message.slice(0, 60)}` }; }
}

function writeRegistry(rows: Row[]): string {
  const path = join(ORCH_DIR, "SERVICE_REGISTRY.json");
  writeFileSync(path, JSON.stringify({
    _schema: "The 25 critical µ-services of the ollamas working principle (modular monolith — SERVICES.md). Regenerated by `tsx orchestration/bin/services.ts --health`.",
    ts: new Date().toISOString(),
    services: SERVICES.map((s) => ({ id: s.id, kind: s.kind, role: s.role, deps: s.deps, source: s.source })),
    network: NETWORK_SERVICES,
    lastHealth: rows,
  }, null, 2) + "\n");
  return path;
}

async function main(): Promise<void> {
  const problems = validateRegistry();
  if (problems.length) {
    console.error(`❌ registry invalid:\n${problems.map((p) => `  - ${p}`).join("\n")}`);
    process.exit(1);
  }

  if (LIST) {
    for (const s of SERVICES) process.stdout.write(`${s.id.padEnd(20)} ${s.kind.padEnd(5)} ${s.role}\n`);
    for (const n of NETWORK_SERVICES) process.stdout.write(`${n.id.padEnd(20)} net   ${n.role}\n`);
    return;
  }

  // --health (default): one by one, streamed to the live tracker.
  const runId = "ollamas:services-health";
  resetRun();
  emitEvent(healthRunEvents(runId, new Date().toISOString()));
  const rows: Row[] = [];

  for (const s of SERVICES) {
    emitEvent({ type: "item", ts: new Date().toISOString(), runId, id: s.id, status: "active" });
    emitEvent({ type: "note", ts: new Date().toISOString(), runId, note: `${s.id} selftest koşuyor`, phase: "selftest" });
    const t0 = Date.now();
    let r: SelftestResult;
    try { r = await s.selftest(); }
    catch (e) { r = { ok: false, evidence: `selftest threw: ${(e as Error).message.slice(0, 100)}` }; }
    const ms = Date.now() - t0;
    rows.push({ id: s.id, kind: s.kind, ok: r.ok, evidence: r.evidence, ms });
    emitEvent({ type: "item", ts: new Date().toISOString(), runId, id: s.id, status: r.ok ? "done" : "failed" });
    if (!JSON_OUT) process.stdout.write(`  ${r.ok ? "✅" : "❌"} ${s.id.padEnd(20)} ${String(ms).padStart(4)}ms  ${r.evidence}\n`);
  }

  if (!OFFLINE) {
    for (const n of NETWORK_SERVICES) {
      const t0 = Date.now();
      const r = await probeNetwork(n.url);
      rows.push({ id: n.id, kind: "network", ok: r.ok, evidence: r.evidence, ms: Date.now() - t0 });
      if (!JSON_OUT) process.stdout.write(`  ${r.ok ? "✅" : "❌"} ${n.id.padEnd(20)} ${String(Date.now() - t0).padStart(4)}ms  ${r.evidence}\n`);
    }
  }

  emitEvent({ type: "note", ts: new Date().toISOString(), runId, note: `sağlık taraması bitti: ${rows.filter((r) => r.ok).length}/${rows.length} ok` });
  emitEvent({ type: "finish", ts: new Date().toISOString(), runId });

  const okCount = rows.filter((r) => r.ok).length;
  const registryPath = writeRegistry(rows);
  if (JSON_OUT) { console.log(JSON.stringify({ ok: okCount === rows.length, okCount, total: rows.length, rows })); }
  else process.stdout.write(`\n${okCount === rows.length ? "✅" : "❌"} ${okCount}/${rows.length} servis sağlıklı · registry → ${registryPath}\n`);
  process.exit(okCount === rows.length ? 0 : 1);
}

main().catch((e) => { console.error("[services] fatal:", (e as Error)?.message ?? e); process.exit(1); });
