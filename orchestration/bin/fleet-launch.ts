#!/usr/bin/env tsx
/**
 * orchestration/bin/fleet-launch.ts — open the local model-fleet across Terminal.app + iTerm2.
 *
 * Computes the ≤2-per-model assignment (lib/fleet-plan.ts), writes one bash wrapper per slot, then
 * (with --go) opens a tab per slot in each app and runs its wrapper. DRY-RUN BY DEFAULT (osascript
 * only fires with --go — respects RISK-ORCH-008 osascript-freeze: opt-in, timeout-bounded).
 *
 * Each worker runs in PROPOSE mode: it produces a patch proposal + notes into an ISOLATED --root
 * under ~/.llm-mission-control/fleet/work (never the repo tree). The conductor (fleet-conduct.ts)
 * verifies + gates before anything is applied — weak models never mutate main. Single-GPU truth:
 * LOCAL slots acquire a shared "gpu" claim (serialize); CLOUD slots run free (parallel).
 *
 * Run:
 *   tsx orchestration/bin/fleet-launch.ts              # dry-run: plan + wrappers, no windows
 *   tsx orchestration/bin/fleet-launch.ts --go         # actually open tabs (all streams)
 *   tsx orchestration/bin/fleet-launch.ts --go --streams typescript-core,shell-harden
 *   tsx orchestration/bin/fleet-launch.ts --cloud-only # dry/go only the cloud slots (GPU-safe)
 */
import { writeFileSync, mkdirSync, chmodSync } from "node:fs";
import { execFileSync } from "node:child_process";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { homedir } from "node:os";
import { buildFleetPlan, assertMaxTwo, STREAMS, type FleetPlan, type Assignment } from "./lib/fleet-plan";
import { readyApiProviders } from "./lib/ready-api";
import { buildMission, DEFAULT_DEPS, type AssignmentLike } from "./lib/mission";
import { orderSlotsByMission, type OrderedSlot } from "./lib/fleet-order";
import { selectWorkspaceRequest, parseWorkspaceResp } from "./lib/workspace";

const HERE = dirname(fileURLToPath(import.meta.url));
const ORCH_DIR = join(HERE, "..");
const REPO = join(ORCH_DIR, "..");
const GO = process.argv.includes("--go");
const SEQUENCED = process.argv.includes("--sequenced");
const CLOUD_ONLY = process.argv.includes("--cloud-only");
const NO_WORKSPACE = process.argv.includes("--no-workspace");
const OLLAMAS_URL = process.env.OLLAMAS_URL || "http://127.0.0.1:3000";
const streamsArg = (() => { const i = process.argv.indexOf("--streams"); return i >= 0 ? (process.argv[i + 1] ?? "").split(",").filter(Boolean) : null; })();
const FLEET_HOME = join(homedir(), ".llm-mission-control", "fleet");
const STEPS = Number(process.env.FLEET_STEPS || 8);

function liveModels(): string[] {
  try {
    return execFileSync("ollama", ["list"], { encoding: "utf8", stdio: ["ignore", "pipe", "ignore"], timeout: 8000 })
      .split("\n").slice(1).map((l) => l.trim().split(/\s+/)[0]).filter((m) => m && m.includes(":"));
  } catch { return []; }
}

/** PROPOSE-mode task prompt for a stream. Proposal is the FINAL MESSAGE (no file-write dependency —
 *  weak/cloud models reliably finish; the conductor materializes PROPOSAL.md from the report). */
function taskPrompt(a: Assignment): string {
  return [
    `You are a PROPOSE-only worker for the ollamas project, stream "${a.stream}" (${a.concern}).`,
    `Language focus: ${a.lang}. Repo: ${REPO}. Do NOT edit any repo file.`,
    `Read AT MOST 2 relevant ${a.lang} files/dirs (list_tree or read), then STOP reading.`,
    `Your FINAL MESSAGE must BE the proposal, in exactly this shape:`,
    `  ## Change: <one concrete high-value change for this stream>`,
    `  ## Diff: <a short unified diff you propose>`,
    `  ## Test: <the test that would prove it>`,
    `Then end with the line: VERDICT: DONE`,
    `Do not write files. Keep it under ~25 lines. Evidence over prose.`,
  ].join("\n");
}

/** Emit a per-slot bash wrapper: dedup-claim → (local: gpu-mutex wait) → dispatch PROPOSE → report → release. */
function writeWrapper(a: Assignment): string {
  const tsx = join(REPO, "node_modules", ".bin", "tsx");
  const root = join(FLEET_HOME, "work", `${a.stream}.${a.slot}`);
  const report = join(FLEET_HOME, "reports", `${a.stream}.${a.slot}.json`);
  const log = join(FLEET_HOME, "logs", `${a.stream}.${a.slot}.log`);
  const wrapper = join(FLEET_HOME, "wrappers", `${a.stream}.${a.slot}.sh`);
  mkdirSync(dirname(wrapper), { recursive: true });
  mkdirSync(root, { recursive: true });
  mkdirSync(dirname(report), { recursive: true });
  mkdirSync(dirname(log), { recursive: true });
  const task = taskPrompt(a).replace(/'/g, `'\\''`);
  const isLocal = a.runtime === "local";
  const sh = `#!/usr/bin/env bash
# fleet worker — ${a.stream} @ ${a.app} (${a.model}, ${a.runtime}). Auto-generated by fleet-launch.ts.
set -uo pipefail
cd ${REPO} || exit 1
export ORCH_TAB="fleet-${a.stream}-${a.slot}"
export OLLAMAS_URL="\${OLLAMAS_URL:-http://127.0.0.1:3000}"  # agent-dispatch default :8090 → our server is :3000
LOG="${log}"
# live progress → .log (tab shows it too via tee); JSON report stays clean on its own file
log(){ echo "[$(date -u +%H:%M:%S)] $*" | tee -a "$LOG"; }
: > "$LOG"
log "🛰  ${a.stream} · ${a.app} · ${a.model} (${a.runtime}) START"
# 1) dedup-claim the (stream|slot) so a double launch cannot run twice
if ! ${tsx} orchestration/bin/claim.ts "${a.stream}" "${a.slot}" 2>&1 | tee -a "$LOG"; then log "collision → exit"; exit 3; fi
${isLocal ? `# 2) single-GPU mutex: only one LOCAL worker at a time (cloud slots skip this)
until ${tsx} orchestration/bin/claim.ts gpu local >/dev/null 2>&1; do log "…GPU busy, waiting"; sleep 5; done
log "🔓 GPU acquired"` : `# cloud slot — no GPU mutex (parallel-safe)`}
# 3) PROPOSE run — stdout=JSON report (clean), stderr=live progress → .log (root-fix: never merge into JSON)
log "▶ dispatch ${a.model} (steps ${STEPS})…"
node scripts/agent-dispatch.mjs '${task}' --provider ollama-local --model "${a.model}" --steps ${STEPS} --root "${root}" --json > "${report}" 2>>"$LOG"
RC=$?
${isLocal ? `${tsx} orchestration/bin/claim.ts --done gpu local >/dev/null 2>&1` : ""}
${tsx} orchestration/bin/claim.ts --done "${a.stream}" "${a.slot}" >/dev/null 2>&1
log "✅ DONE report → ${report} (rc=$RC)"
`;
  writeFileSync(wrapper, sh);
  chmodSync(wrapper, 0o755);
  return wrapper;
}

/** AppleScript to open a NEW tab and run a command. iTerm2: create a window if none is open (root-fix
 *  for silent skip when no window existed). The command itself keeps the tab alive (persistent agent). */
function openTab(app: Assignment["app"], cmd: string): void {
  const inner = cmd.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
  const script = app === "Terminal.app"
    ? `tell application "Terminal"\n  activate\n  do script "${inner}"\nend tell`
    : `tell application "iTerm"\n  activate\n  if (count of windows) = 0 then\n    create window with default profile\n    tell current session of current window to write text "${inner}"\n  else\n    tell current window\n      create tab with default profile\n      tell current session to write text "${inner}"\n    end tell\n  end if\nend tell`;
  execFileSync("osascript", ["-e", script], { timeout: 15000 });
}

function renderPlanMd(plan: FleetPlan): string {
  const L = [
    `# FLEET_PLAN.md — Local model-fleet (Terminal.app + iTerm2)`,
    ``, `> Auto: \`tsx orchestration/bin/fleet-launch.ts\`. Single-GPU: local slots serialize (gpu claim); cloud parallel.`,
    `> ≤2 streams/model: ${plan.maxTwoOk ? "✅ OK" : "❌ VIOLATED"} · local slots ${plan.localSlots} · cloud slots ${plan.cloudSlots}`,
    ``, `| Stream | Concern | Terminal.app | iTerm2 |`, `|--------|---------|--------------|--------|`,
  ];
  for (const s of STREAMS) {
    const t = plan.assignments.find((a) => a.stream === s.id && a.slot === "terminal");
    const i = plan.assignments.find((a) => a.stream === s.id && a.slot === "iterm2");
    const fmt = (a?: Assignment) => a?.model ? `\`${a.model}\` (${a.runtime})` : "—";
    L.push(`| ${s.id} | ${s.concern} | ${fmt(t)} | ${fmt(i)} |`);
  }
  L.push(``, `## Per-model load (≤2)`, ...plan.perModel.map((p) => `- \`${p.model}\` → ${p.streams.length}: ${p.streams.join(", ")}`));
  if (plan.unassigned.length) L.push(``, `## ⚠️ Unassigned slots`, ...plan.unassigned.map((u) => `- ${u.stream}/${u.slot}`));
  return L.join("\n");
}

/** Point the ollamas server's agent workspace at the REPO so fleet workers can read repo files (their read
 *  tools are confined to db.data.workspacePath). Best-effort: warns + continues if the server is down. */
async function ensureWorkspace(): Promise<void> {
  if (NO_WORKSPACE) { console.log("  ↳ workspace: skipped (--no-workspace)"); return; }
  const req = selectWorkspaceRequest(OLLAMAS_URL, REPO);
  try {
    const res = await fetch(req.url, { method: req.method, headers: { "Content-Type": req.contentType }, body: req.body, signal: AbortSignal.timeout(4000) });
    const r = parseWorkspaceResp(await res.text());
    console.log(r.ok ? `  ↳ workspace → ${r.workspacePath} (fleet workers can read the repo)` : `  ⚠ workspace set failed: ${r.error}`);
  } catch (e: any) {
    console.log(`  ⚠ workspace set skipped (server unreachable at ${OLLAMAS_URL}): ${(e?.message ?? e).slice(0, 60)}`);
  }
}

async function main(): Promise<void> {
  // Key-live free-tier API providers -> `provider::model` prefer entries become usable
  // fleet workers (cloud runtime, no GPU ticket). Server down/no keys -> [] -> legacy plan.
  const plan = buildFleetPlan(liveModels(), await readyApiProviders(OLLAMAS_URL));
  try { assertMaxTwo(plan); } catch (e: any) { console.error(`[fleet-launch] ${e.message}`); process.exit(1); }
  if (GO) await ensureWorkspace(); // only mutate server workspace when actually opening the fleet

  let slots = plan.assignments.filter((a) => a.model);
  if (streamsArg) slots = slots.filter((a) => streamsArg.includes(a.stream));
  if (CLOUD_ONLY) slots = slots.filter((a) => a.runtime === "cloud");

  // --sequenced: order the tabs by the sequenced ethical MISSION (T1→Tn dependency order, tier-annotated)
  // so the live fleet opens foundation-first instead of arbitrarily. Backward-compatible: without the flag
  // the launch order is unchanged. Each launch line carries its T#/tier so the operator sees the sequence.
  let launch: { a: Assignment; label: string }[] = slots.map((a) => ({ a, label: "" }));
  if (SEQUENCED) {
    const assignLike: AssignmentLike[] = plan.assignments.map((a) => ({ stream: a.stream, concern: a.concern, model: a.model }));
    const mission = buildMission(assignLike, new Map(Object.entries(DEFAULT_DEPS)));
    const ordered: OrderedSlot<Assignment>[] = orderSlotsByMission(slots, mission);
    launch = ordered.map((o) => ({ a: o.slot, label: `T${o.missionOrder}·${o.tier || "?"}` }));
  }

  for (const { a } of launch) writeWrapper(a); // keep one-shot wrappers on disk for fleet-conduct re-dispatch compat

  mkdirSync(ORCH_DIR, { recursive: true });
  writeFileSync(join(ORCH_DIR, "FLEET_PLAN.json"), JSON.stringify({ ts: new Date().toISOString().slice(0, 19) + "Z", sequenced: SEQUENCED, plan }, null, 2) + "\n");
  writeFileSync(join(ORCH_DIR, "FLEET_PLAN.md"), renderPlanMd(plan) + "\n");

  console.log(`🛰  fleet-launch [${GO ? "GO" : "dry-run"}]${SEQUENCED ? " [sequenced T1→Tn]" : ""} · ${launch.length} slot · local ${plan.localSlots}/cloud ${plan.cloudSlots} · ≤2/model ${plan.maxTwoOk ? "✅" : "❌"}`);
  const tsx = join(REPO, "node_modules", ".bin", "tsx");
  for (const { a, label } of launch) {
    const tag = label ? `${label} ` : "";
    // each tab runs the PERSISTENT living agent (not the one-shot wrapper) so it stays open + followable;
    // trailing `exec $SHELL` guarantees the tab survives even if the agent ever returns.
    const cmd = `cd ${REPO} && ${tsx} orchestration/bin/fleet-agent.ts ${a.stream} ${a.slot}; exec $SHELL`;
    if (!GO) { console.log(`  [dry] ${tag}${a.app} · ${a.stream} · ${a.model} (${a.runtime}) → fleet-agent ${a.stream} ${a.slot}`); continue; }
    try { openTab(a.app, cmd); console.log(`  ▶ opened ${tag}${a.app} · ${a.stream} · ${a.model} (persistent agent)`); }
    catch (e: any) { console.error(`  ✗ ${a.app} ${a.stream}: ${(e?.message ?? "osascript hata").slice(0, 60)}`); }
  }
  if (!GO) console.log(`\nDry-run. Gerçek aç: --go [--sequenced] [--cloud-only] [--streams a,b]. Kondüktör: tsx orchestration/bin/fleet-conduct.ts`);
}

main().catch((e) => { console.error(`[fleet-launch] ${e?.message ?? e}`); process.exit(1); });
