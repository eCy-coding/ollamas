#!/usr/bin/env tsx
/**
 * orchestration/bin/claude-dispatch.ts — vO41 otonom Claude Code conductor ZİNCİRİ (IO shell).
 *
 * conduct/fuse'un tespit ettiği EN KRİTİK gereksinimi alır → YENİ Terminal.app/iTerm2 sekmesinde
 * `claude --permission-mode plan` (conductor prompt'la) açar. vO41 kesintisiz zincir: bir görev
 * biter (evidence-based auto-complete: hedef taze REQUIREMENTS'tan kaybolur VEYA oturum done yazar)
 * → sıradaki gereksinim ANINDA dispatch edilir. §3.1(c) koordinasyon sinyali.
 *
 * GÜVENLİK KATMANLARI (billing-runaway imkânsız):
 *   dry-run DEFAULT → gerçek spawn = `--go` VE (.claude-dispatch-enabled marker VEYA ORCH_CLAUDE_GO=1)
 *   kill-switch `.claude-dispatch-off` · target idempotency · cap=1 · 24h bütçe 6 spawn ·
 *   failure-backoff 4h (yalnız stale sonrası; done anında zincirler) · 2× stale → blocked + notify (tek sefer)
 *   REQUIREMENTS bayatsa (>60 dk) spawn YOK · prompt DOSYAYA yazılır, AppleScript'e inline edilmez
 *   spawn dosyaları ~/.ollamas/ altında (launchd WatchPaths ~/.llm-mission-control DIŞI —
 *   vO40 self-retrigger churn KÖK-FIX) · audit dedup (ardışık özdeş satır yazılmaz)
 *
 * Çalıştır:
 *   tsx orchestration/bin/claude-dispatch.ts              # dry-run: karar + CLAUDE_DISPATCH.md
 *   tsx orchestration/bin/claude-dispatch.ts --go         # marker varsa gerçek spawn (tek sekme)
 *   tsx orchestration/bin/claude-dispatch.ts --go --app iterm2
 */
import { readFileSync, writeFileSync, appendFileSync, existsSync, mkdirSync, chmodSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { homedir } from "node:os";
import { sourceFresh, type Requirement } from "./lib/fuse";
import { openTab, type SpawnApp } from "./lib/tab-spawn";
import { notify } from "./lib/signal";
import {
  foldSessions, reconcileSessions, autoCompleteSessions, staleCounts, spawnsInWindow, shouldAudit,
  planDispatch, buildDispatchPrompt, renderDispatchMd, taskFingerprint, nextPending,
  type DispatchSession,
} from "./lib/claude-dispatch";

const HERE = dirname(fileURLToPath(import.meta.url));
const ORCH_DIR = join(HERE, "..");
const REPO = join(ORCH_DIR, "..");
const GO = process.argv.includes("--go");
const APP: SpawnApp = (() => { const i = process.argv.indexOf("--app"); return i >= 0 && /iterm/i.test(process.argv[i + 1] ?? "") ? "iTerm2" : "Terminal.app"; })();
const STATE_F = join(ORCH_DIR, "seyir", "claude-dispatch-state.jsonl");
const AUDIT_F = join(ORCH_DIR, "seyir", "dispatch-log.jsonl");
const KILL_F = join(ORCH_DIR, ".claude-dispatch-off");
const MARKER_F = join(ORCH_DIR, ".claude-dispatch-enabled");
// vO41 KÖK-FIX: launchd WatchPaths ~/.llm-mission-control'ü izler; vO40 spawn dosyalarını ORAYA
// yazıyordu → her spawn/dry kendi autopilot zincirini re-trigger ediyordu (1 satır/dk churn).
const HOME_DIR = join(homedir(), ".ollamas", "claude-dispatch");
// Fable conductor DEFAULT (Emre direktifi); ORCH_CLAUDE_MODEL=default → CLI kendi default'unu kullanır.
const MODEL = process.env.ORCH_CLAUDE_MODEL ?? "claude-fable-5";
// vO42 zero-question: spawn edilen oturum SORMAZ (Emre'nin açık talimatı). Geri dönüş: env=plan|acceptEdits.
const PERMISSION_MODE = process.env.ORCH_CLAUDE_PERMISSION_MODE ?? "bypassPermissions";
// 24h spawn bütçesi (kesintisizlik ↔ maliyet ayarı Emre'de).
const MAX_PER_DAY = Number(process.env.ORCH_CLAUDE_MAX_PER_DAY) || 6;

function readJson(p: string): any { try { return JSON.parse(readFileSync(p, "utf8")); } catch { return null; } }
function readLines(p: string): string[] { try { return readFileSync(p, "utf8").split("\n").filter(Boolean); } catch { return []; } }
function appendJsonl(p: string, entry: Record<string, unknown>): void {
  try { mkdirSync(dirname(p), { recursive: true }); appendFileSync(p, JSON.stringify(entry) + "\n"); } catch { /* best-effort */ }
}
/** Audit + churn-dedup: ardışık özdeş (action+fingerprint+reason) satır YAZILMAZ. */
function audit(entry: { ts: string; action: string; fingerprint?: string | null; reason?: string; [k: string]: unknown }): void {
  const last = readLines(AUDIT_F).pop();
  if (shouldAudit(last, entry)) appendJsonl(AUDIT_F, entry);
}

/** Spawn wrapper: prompt dosyadan okunur → AppleScript'e asla inline prompt gitmez. */
function writeSpawnFiles(fp: string, prompt: string): string {
  mkdirSync(HOME_DIR, { recursive: true });
  const promptF = join(HOME_DIR, `${fp}.prompt.md`);
  const wrapper = join(HOME_DIR, `${fp}.sh`);
  writeFileSync(promptF, prompt);
  const modelFlag = MODEL === "default" ? "" : ` --model ${MODEL}`;
  writeFileSync(wrapper, [
    `#!/usr/bin/env bash`,
    `# claude-dispatch conductor session — auto-generated by claude-dispatch.ts (vO41). Fingerprint ${fp}.`,
    `set -uo pipefail`,
    `cd ${REPO} || exit 1`,
    `claude --permission-mode ${PERMISSION_MODE}${modelFlag} "$(cat '${promptF}')"`,
    ``,
  ].join("\n"));
  chmodSync(wrapper, 0o755);
  return wrapper;
}

function main(): void {
  const ts = new Date().toISOString();
  const nowMs = Date.now();

  // 1) Ledger: raw history (escalation+bütçe) + LWW fold + stale-reconcile
  const rawLines = readLines(STATE_F);
  const sessions = foldSessions(rawLines);
  const applyChange = (s: DispatchSession, action: string, reason?: string) => {
    appendJsonl(STATE_F, s as unknown as Record<string, unknown>);
    audit({ ts, action, fingerprint: s.fingerprint, reason });
    const idx = sessions.findIndex((x) => x.fingerprint === s.fingerprint);
    if (idx >= 0) sessions[idx] = s;
  };
  for (const s of reconcileSessions(sessions, nowMs)) applyChange(s, "stale-reconcile", s.task);

  // 2) Top requirement — tazelik-guard'lı (bayat REQUIREMENTS'a oturum AÇMA)
  const reqJson = readJson(join(ORCH_DIR, "REQUIREMENTS.json"));
  const reqFresh = sourceFresh(reqJson?.ts, 60, nowMs);
  const allReqs: Requirement[] = Array.isArray(reqJson?.requirements) ? reqJson.requirements : [];
  const req: Requirement | null = reqFresh ? (reqJson?.top ?? null) : null;
  const reqStale = !!reqJson && !reqFresh;

  // 3) vO41 SUCCESS FUNCTION: hedefi taze+dolu REQUIREMENTS'tan kaybolan aktif oturum = done
  const freshTargets = new Set(allReqs.map((r) => r.target));
  for (const s of autoCompleteSessions(sessions, freshTargets, { reqsFresh: reqFresh, reqsNonEmpty: allReqs.length > 0, nowMs })) {
    applyChange(s, "auto-complete", "hedef taze REQUIREMENTS'ta artık yok — çözüldü");
  }

  // 4) Karar (pure) — zincir girdileri: lastStatus / 24h bütçe / escalation sayacı
  const killSwitch = existsSync(KILL_F);
  const goEnabled = GO && (existsSync(MARKER_F) || process.env.ORCH_CLAUDE_GO === "1");
  const lastSession = [...sessions].sort((a, b) => Date.parse(b.startedTs) - Date.parse(a.startedTs))[0];
  const spawns24h = spawnsInWindow(rawLines, nowMs);
  const plan = planDispatch({
    sessions, req, nowMs, killSwitch, goEnabled, maxPerDay: MAX_PER_DAY,
    lastStatus: lastSession?.status, spawns24h,
    staleCountForReq: req ? (staleCounts(rawLines).get(taskFingerprint(req)) ?? 0) : 0,
  });
  if (reqStale && plan.mode === "skip" && !req) plan.reason = "REQUIREMENTS.json bayat (>60 dk) — önce autopilot/fuse taze koş";

  // 5) Eylem
  if (plan.mode === "blocked" && req && plan.fingerprint) {
    // Escalation: BİR kez blocked yaz + notify (LWW zaten blocked ise sus — spam yok)
    const already = sessions.find((s) => s.fingerprint === plan.fingerprint)?.status === "blocked";
    if (!already) {
      applyChange({ fingerprint: plan.fingerprint, task: `${req.criticality}:${req.target}`, target: req.target, app: "-", startedTs: ts, status: "blocked" }, "blocked", plan.reason);
      notify(`claude-dispatch BLOCKED: ${req.target} — 2× stale, insan müdahalesi gerekli`);
    }
    console.log(`🛑 ${plan.reason}`);
  } else if (plan.mode === "spawn" && req && plan.fingerprint) {
    const prompt = buildDispatchPrompt(req, readJson(join(ORCH_DIR, "MODEL_SELECTION.json")), REPO);
    const wrapper = writeSpawnFiles(plan.fingerprint, prompt);
    try {
      openTab(APP, `${wrapper}; exec $SHELL`);
      const session: DispatchSession = { fingerprint: plan.fingerprint, task: `${req.criticality}:${req.target}`, target: req.target, app: APP, startedTs: ts, status: "active" };
      appendJsonl(STATE_F, session as unknown as Record<string, unknown>);
      sessions.push(session);
      audit({ ts, action: "spawn", fingerprint: plan.fingerprint, target: req.target, app: APP, wrapper });
      console.log(`▶ spawned ${APP} · claude plan-mode conductor · ${req.criticality}:${req.target} (${plan.fingerprint})`);
    } catch (e: any) {
      plan.reason = `osascript hata: ${(e?.message ?? "").slice(0, 60)} (Automation izni?)`;
      audit({ ts, action: "spawn-fail", fingerprint: plan.fingerprint, reason: plan.reason });
      console.error(`✗ ${plan.reason}`);
    }
  } else {
    audit({ ts, action: plan.mode, fingerprint: plan.fingerprint ?? null, reason: plan.reason });
    console.log(plan.mode === "dry" ? `[dry] ${plan.reason} → görev: ${req ? req.criticality + ":" + req.target : "-"}` : `⏭ ${plan.reason}`);
  }

  // 6) vO42 paralel ön-hesap: mevcut görev koşarken SIRADAKİ görevin prompt+wrapper'ı önceden hazır
  //    (spawn anında fingerprint uyuşmazsa zaten taze render edilir — prefetch görünürlük + sıfır-boşluk).
  const pending = reqFresh ? nextPending(allReqs, sessions) : null;
  const hasActive = sessions.some((s) => s.status === "active");
  const nextUp = hasActive && pending && pending.target !== req?.target ? pending : null;
  if (nextUp) {
    try { writeSpawnFiles(taskFingerprint(nextUp), buildDispatchPrompt(nextUp, readJson(join(ORCH_DIR, "MODEL_SELECTION.json")), REPO)); } catch { /* prefetch best-effort */ }
  }

  // 7) Rapor (her koşuda)
  const md = renderDispatchMd({ ts, plan, req, sessions, killSwitch, goEnabled, app: APP, reqStale, spawns24h, maxPerDay: MAX_PER_DAY, nextUp });
  writeFileSync(join(ORCH_DIR, "CLAUDE_DISPATCH.md"), md + "\n");
}

try { main(); } catch (e: any) { console.error(`[claude-dispatch] ${e?.message ?? e}`); } // never-throw (autopilot adımı)
