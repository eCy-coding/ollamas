#!/usr/bin/env node
// scripts/gen-promptfoo-providers.mjs — regenerate eval/promptfooconfig.yaml from
// server/provider-catalog.ts. Zero-dep (Node >=24). promptfoo itself is dev-time only,
// run via `npx -y promptfoo@latest eval -c eval/promptfooconfig.yaml` — never a runtime dep.
//
// Provider selection at generation time:
//   • keyless entries (pollinations) are always included — the $0 smoke floor
//   • key'd entries are included only when their envKey is present in this shell
//   • entries with an unresolved {account_id} placeholder are skipped
// Usage: node scripts/gen-promptfoo-providers.mjs [--all]   (--all = include key'd providers
// regardless of env, for teams running with a loaded vault shell)
//
// Ported from ollamas-integrate-wt (B6). This branch has no scripts/provider-catalog-sync.mjs
// (upstream drift-check tooling is a separate, unshipped lane here) so `parseCatalog` is
// inlined below instead of imported — same regex-over-TS-source approach, zero-dep, no
// TS loader required.

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");

/** Parse PROVIDER_CATALOG entries out of the TS source (no import — keeps this script runnable
 *  without a TS loader and immune to server-side module side effects). Mirrors
 *  provider-catalog-sync.mjs's parseCatalog in ollamas-integrate-wt. */
export function parseCatalog(tsSource) {
  const entries = [];
  // Each entry body: id: "x", ...comments allowed..., baseUrl: "...", envKey: "...",
  // defaultModel: "y", limits: {...}. `(?:\s|\/\/[^\n]*)*` tolerates interleaved // comments.
  const gap = String.raw`(?:\s|\/\/[^\n]*)*`;
  const re = new RegExp(
    `id:\\s*"([^"]+)",${gap}baseUrl:\\s*"([^"]+)"[\\s\\S]*?envKey:\\s*"([^"]+)"[\\s\\S]*?defaultModel:\\s*"([^"]+)"[\\s\\S]*?limits:\\s*\\{([^}]*)\\}([\\s\\S]*?)\\n  \\}`,
    "g",
  );
  for (const m of tsSource.matchAll(re)) {
    const [, id, baseUrl, envKey, defaultModel, limitsBody, tail] = m;
    const perMin = Number(/perMin:\s*([\d_]+)/.exec(limitsBody)?.[1]?.replaceAll("_", "") ?? 0);
    const perDay = Number(/perDay:\s*([\d_]+)/.exec(limitsBody)?.[1]?.replaceAll("_", "") ?? 0);
    const keyless = /keyless:\s*true/.test(tail);
    entries.push({ id, baseUrl, envKey, defaultModel, perMin, perDay, keyless });
  }
  return entries;
}

// Eval-side base-URL overrides for ANONYMOUS (keyless) access — escape hatch for when a
// catalog baseUrl breaks for anonymous traffic before the catalog itself is fixed. Empty on
// this branch: server/provider-catalog.ts is used as-is (no known anonymous-traffic break).
export const KEYLESS_BASE_OVERRIDES = {};

export function selectProviders(entries, env, { all = false } = {}) {
  return entries.filter((e) => {
    if (e.baseUrl.includes("{account_id}")) return false;
    if (e.keyless) return true;
    return all || Boolean(env[e.envKey]);
  });
}

export function renderConfig(selected) {
  const providers = selected
    .map((e) => {
      const baseUrl = (e.keyless && KEYLESS_BASE_OVERRIDES[e.id]) || e.baseUrl;
      const lines = [
        `  - id: openai:chat:${e.defaultModel}`,
        `    label: ${e.id}`,
        `    config:`,
        `      apiBaseUrl: ${baseUrl}`,
      ];
      if (e.keyless) {
        // promptfoo requires an apiKey value for openai-compat providers; keyless endpoints ignore it.
        lines.push(`      apiKey: keyless`);
      } else {
        lines.push(`      apiKeyEnvar: ${e.envKey}`);
      }
      lines.push(`      max_tokens: 64`, `      temperature: 0`);
      return lines.join("\n");
    })
    .join("\n");

  return `# eval/promptfooconfig.yaml — GENERATED by scripts/gen-promptfoo-providers.mjs. Do not hand-edit;
# re-run the generator after changing server/provider-catalog.ts.
# Run: npx -y promptfoo@latest eval -c eval/promptfooconfig.yaml --no-cache
description: ollamas free-provider smoke matrix (catalog-derived)

providers:
${providers}

prompts:
  - "{{task}}"

tests:
  - description: echo discipline
    vars:
      task: "Reply with exactly the word PONG and nothing else."
    assert:
      - type: icontains
        value: PONG
  - description: deterministic arithmetic
    vars:
      task: "What is 7*6? Reply with the number only."
    assert:
      - type: contains
        value: "42"
  - description: json shape
    vars:
      task: 'Return only a JSON object: {"ok": true}. No prose, no code fences.'
    assert:
      # Providers with reasoning leakage prepend prose (which may itself quote JSON) —
      # take the LAST { ... } span, the actual answer by convention.
      - type: javascript
        value: "JSON.parse(output.slice(output.lastIndexOf('{'), output.lastIndexOf('}') + 1)).ok === true"
`;
}

async function main() {
  const all = process.argv.includes("--all");
  const entries = parseCatalog(readFileSync(join(ROOT, "server/provider-catalog.ts"), "utf8"));
  if (entries.length === 0) throw new Error("parsed 0 catalog entries");
  const selected = selectProviders(entries, process.env, { all });
  if (selected.length === 0) throw new Error("no providers selected — catalog lost its keyless floor?");
  mkdirSync(join(ROOT, "eval"), { recursive: true });
  writeFileSync(join(ROOT, "eval/promptfooconfig.yaml"), renderConfig(selected));
  console.log(
    `eval/promptfooconfig.yaml: ${selected.length}/${entries.length} providers → ${selected.map((e) => e.id).join(", ")}`,
  );
}

if (process.argv[1] && import.meta.url === new URL(`file://${process.argv[1]}`).href) {
  main().catch((err) => {
    console.error(`gen-promptfoo-providers failed: ${err.message}`);
    process.exit(2);
  });
}
